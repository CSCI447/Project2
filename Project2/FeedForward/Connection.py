from Project2.FeedForward import Neuron

class Connection:
    def __init__(self):
        weight = 0
        delta = 0
        fromNeuron =  Neuron

    def getWeight(self):
        return self.weight

    def setWeight(self, value):
        self.weight = value

    def getDelta(self):
        return self.delta

    def setDelta(self, value):
        self.delta = value

    def getFromNeuron(self):
        return self.fromNeuron

    def setFromNeuron(self, value):
        self.fromNeuron = value

